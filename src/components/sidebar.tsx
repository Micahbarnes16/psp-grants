function InboxBadge() {
  const count = useQuery(api.grants.getInboxCount);
  if (!count) return null;
  return (
    <span className="ml-auto rounded-full bg-gray-900 px-1.5 py-0.5 text-[10px] font-semibold leading-none text-white">
      {count > 99 ? "99+" : count}
    </span>
  );
}

